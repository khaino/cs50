
city_name = "Saint peter port port port"

# convert to lower & print
print(city_name.lower())
# convert to upper & print
print(city_name.upper())
# convert to titile format 'Saint Peter Port'
print(city_name.title())
# replace space ' ' with '*'. Saint peter port => Saint*peter*port'
print(city_name.replace(" ", "*", 2))

